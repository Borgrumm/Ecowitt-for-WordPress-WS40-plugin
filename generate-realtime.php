Purpose
*******************************************************************************
This program generates a Cumulus-compatible realtime.txt file for the WS40
WordPress plugin from the latest Ecowitt structured JSON file.

Input:
    - data/latest.json          created by ecowitt-upload.php V2
    - config.ini                runtime configuration, paths, timezone
    - ecowitt-map.json          alias map used as fallback when latest.json was
                                generated by an older uploader or lacks a metric

Output:
    - data/realtime.txt         Cumulus realtime.txt, one space-separated line
    - data/realtime-state.json  daily state for min/max, windrun, yesterday rain
    - log/YYYY-MM-DD.log        optional action/error log

It can be called either:
    - by cron, e.g. every minute
    - by ecowitt-upload.php after a successful upload

Recommended architecture:
    GW3000/WS90 -> ecowitt-upload.php V2 -> latest.json -> generate-realtime.php V2 -> realtime.txt

The station/gateway should ideally run in UTC. This script converts station UTC
time to the local timezone defined in config.ini for display, daily reset and
min/max timestamps.

*******************************************************************************
Version:
    2.00  :  2026-07-08  config.ini + ecowitt-map.json architecture
                            - reads paths/timezone from config.ini
                            - reads ecowitt-map.json for fallback metric decoding
                            - supports latest.json V2 and older latest.json files
                            - keeps WS40 field alignment from V1.02
                            - uses station UTC time converted to local timezone
                            - namespace isolated for safe require/require_once usage
    1.02  :  2026-07-07  WS40 field-alignment fixes (windGust, ET, humidex, native
                         max daily gust, tracked solar max, configurable timezone)
